// translate_repository_cubit2_state.dart
part of 'translate_repository2_cubit.dart';


abstract class TranslateRepositoryCubit2State extends Equatable {
  const TranslateRepositoryCubit2State();

  @override
  List<Object> get props => [];
}

class TranslateRepositoryInitial2 extends TranslateRepositoryCubit2State {}

class TranslateRepositoryLoading2 extends TranslateRepositoryCubit2State {}

class TranslateRepositoryLoaded2 extends TranslateRepositoryCubit2State {
  final List<Ayat> ayaList;

  const TranslateRepositoryLoaded2({required this.ayaList});

  @override
  List<Object> get props => [ayaList];
}

class TranslateRepositoryError2 extends TranslateRepositoryCubit2State {
  final String message;

  const TranslateRepositoryError2({required this.message});

  @override
  List<Object> get props => [message];
}
